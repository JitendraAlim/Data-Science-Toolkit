# Write A Program To Create String "Hello!!!,(How are you...?).Good Morning" And Count The Occurrence Of Letter O


x = "Hello!!!,(How are you...?).Good Morning"
counter = 0

for i in x:
    if i == "o":
        counter = counter + 1
    else:
        continue
print(counter)


