# Write A Program To Create A String "We are studying Python" and Print First Character, Last Character, First 10 Characters, 3rd To 2nd Last Character


x = "We are studying Python"

# Print First Character
print(x[0])

# Print Last Character
print(x[-1])

# Print First 10 Characters
print(x[0:10])

# Print 3rd To 2nd Last Character
print(x[2:-1])
